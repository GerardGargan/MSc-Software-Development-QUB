public class VarsByExample {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Declare variable
		byte myByte = 56;
		
		//Print variable
		System.out.println("The initial number is : "+myByte);
		
		//Re-declare
		myByte = 100;
		
		System.out.println("The second number is : "+myByte);
		
		

	}

}
