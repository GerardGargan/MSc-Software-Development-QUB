package conditions;

public class SwitchExample {

	public static void main(String[] args) {

		int number = 1;

		switch (number) {

		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("Three");
			break;
		default:
			System.out.println("unknown");

		}
		

	}

}
