package practical4;

public class Part3_3 {

	public static void main(String[] args) {
		for(int i=10; i>=1; i--) {
			System.out.print(i+", ");
			
		}
		System.out.println("Liftoff!");
		
		//using a while loop
		System.out.println();
		
		int count = 10;
		while(count>=1) {
			System.out.print(count+", ");
			count--;
		}
		System.out.println("Liftoff!");

	}

}
